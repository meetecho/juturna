class PixelFormat:
    # RGB/BGR formats (24-bit)
    RGB24 = 'rgb24'
    BGR24 = 'bgr24'

    # RGBA formats (32-bit with alpha)
    RGBA = 'rgba'
    ARGB = 'argb'
    BGRA = 'bgra'
    ABGR = 'abgr'

    # YUV formats
    YUV420P = 'yuv420p'
    YUV444P = 'yuv444p'
    YUYV422 = 'yuyv422'

    # Grayscale
    GRAY = 'gray'
    GRAY8 = 'gray8'

    # Other
    PAL8 = 'pal8'
    GBRP = 'gbrp'